
public class Passwort {

	
	String passwort;
	String username;
	String Website;
	
	public String getUser() {
		return username;
	}
	public String getPass() {
		return passwort;
	}
	public String getWeb() {
		return Website;
	}
	public void setWeb(String pWeb) {
		Website = pWeb;
	}
	public void setPass(String pPass) {
		passwort = pPass;
	}
	public void setUser(String pUser) {
		username = pUser;
	}
	
	
}

